package com.company;

public class Calculator {
    public static int add(int a,int b){
        int sum=0;
        sum=a+b;
        return sum;
    }
    public static int subtract(int a,int b){
        int num=0;
        num=a-b;
        return num;
    }
    public static int multiply(int a,int b){
        int mul=0;
        mul=a*b;
        return mul;
    }
    public static int divide(int a,int b){
        int div=0;
        div=a/b;
        return div;
    }




}
